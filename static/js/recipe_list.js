$(document).ready(function(){
  $("img:first").mouseenter(function(){
    $("#img1-caption").delay(500).slideDown("slow");
  });
  $("img:first").mouseleave(function(){
    $("#img1-caption").delay(1000).slideUp("slow");
  });
});

